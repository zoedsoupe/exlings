# Comments
#
# Comments are notes in your code that are ignored by the computer.
# They're for humans to read and understand what the code does!
#
# In Elixir, there's only one type of comment: the line comment.
#
# Line Comments:
# - Start with the # symbol
# - Everything after # on that line is ignored
# - Great for explaining your code to others (and your future self!)
#
# Examples:
#   # This is a comment
#   IO.puts("Hello")  # This is also a comment (after code)
#
# Pro tip: Good comments explain WHY you did something, not just WHAT
# the code does. The code itself shows WHAT it does!
